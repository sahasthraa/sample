public class if{

static int marks=76;

public static void main(String args[]){
    if(marks>0);
    System.out.println("positive");
    if(marks<0);
    System.out.println("negative");
    if(marks==0);
    System.out.println("zero");
    if(marks%2==0);
    System.out.println("even");
    if(marks%2!=0);
    System.out.println("odd");
    if(marks>100);
    System.out.println("large number");
}
}